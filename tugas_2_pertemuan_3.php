      <h4>4 Komponen tag html</h4>

      <!-- tag ol -->
      <ul><li><i>Struktural</i>. Tanda yang menentukan level atau tingkatan dari sebuah tulisan (contoh, <code>&lt;h1&gt;Golf&lt;/h1&gt;</code> akan memerintahkan peramban untuk menampilkan "Golf" sebagai tulisan tebal besar yang menunjukkan sebagai Heading 1</li>
         <li><i>Presentasional</i>. Tanda yang menentukan tampilan dari sebuah tulisan tidak peduli dengan level dari tulisan tersebut (contoh, <code>&lt;b&gt;boldface&lt;/b&gt;</code> akan menampilkan <b>bold</b>. Tanda presentasional saat ini sudah mulai digantikan oleh <a href="/wiki/Cascading_Style_Sheets" title="Cascading Style Sheets">CSS</a> dan tidak direkomendasikan untuk mengatur tampilan tulisan,</li>
         <li><i><a href="/wiki/Hiperteks" title="Hiperteks">Hiperteks</a></i>. Tanda yang menunjukkan pranala ke bagian dari dokumen tersebut atau pranala ke dokumen lain (contoh, <code>&lt;a href="http://www.wikipedia.org/"&gt; Wikipedia&lt;/a&gt;</code> akan menampilkan <a class="external text" href="https://www.wikipedia.org">Wikipedia</a> sebagai sebuah <a href="/wiki/Hyperlink" class="mw-redirect" title="Hyperlink">hyperlink</a> ke <a href="/wiki/URL" class="mw-redirect" title="URL">URL</a> tertentu),</li>
         <li>Elemen <i>widget</i> yang membuat objek-objek lain seperti tombol (<code>&lt;button&gt;</code>), daftar (<code>&lt;li&gt;</code>), dan garis horizontal (<code>&lt;hr&gt;</code>). Konsep hiperteks pada HTML memungkinkan pembuatan tautan pada suatu kelompok kata atau frasa untuk menuju ke bagian manapun dalam World Wide Web,</li></ul>

